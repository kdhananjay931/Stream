

class OddEven {


    public static void main(String[] args) {


      int a[]={3,5,7,9,12,23,22,56,44};

      for(int i=0;i<a.length;i++){

          if(a[i]%2==0){

              System.out.println("Even Number "+ a[i]);
          }else {

              System.out.println("Odd number " +a[i]);
          }

      }


    }
}
